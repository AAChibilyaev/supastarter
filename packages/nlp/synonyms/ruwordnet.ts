export const BUILTIN_RU_ENTRY_COUNT = Object.keys(BUILTIN_RU_SYNONYMS).length;

/**
 * Russian RuWordNet-style thesaurus for synonym expansion.
 */
export class RuWordNet {
	constructor(private options: RuWordNetOptions = {}) {}

	/**
	 * Find synonyms for a given Russian word.
	 */
	lookup(word: string, overrides?: RuWordNetOptions): SynonymResult[] {
		const opts = { ...this.options, ...overrides };
		const maxResults = opts.maxResults ?? 10;
		const minSimilarity = opts.minSimilarity ?? 0.5;

		const normalized = word.toLowerCase().trim();
		const entries = BUILTIN_RU_SYNONYMS[normalized];
		if (!entries) {
			return [];
		}

		const results: SynonymResult[] = [];

		for (const [pos, synonyms] of Object.entries(entries)) {
			for (const synonym of synonyms) {
				if (results.length >= maxResults) break;
				results.push({
					word: synonym,
					source: "wordnet",
					pos: pos as SynonymResult["pos"],
					similarity: 0.8,
				});
			}
			if (results.length >= maxResults) break;
		}

		return results;
	}

	/**
	 * Check if a word exists in the Russian thesaurus.
	 */
	has(word: string): boolean {
		return word.toLowerCase().trim() in BUILTIN_RU_SYNONYMS;
	}

	/**
	 * Get the total number of entries in the built-in Russian dictionary.
	 */
	get entryCount(): number {
		return BUILTIN_RU_ENTRY_COUNT;
	}

	/**
	 * Add custom Russian synonym entries.
	 */
	addEntries(entries: Record<string, Record<string, string[]>>): void {
		for (const [word, posMap] of Object.entries(entries)) {
			const normalized = word.toLowerCase().trim();
			if (!BUILTIN_RU_SYNONYMS[normalized]) {
				(BUILTIN_RU_SYNONYMS as Record<string, Record<string, string[]>>)[normalized] = {};
			}
			for (const [pos, synonyms] of Object.entries(posMap)) {
				const existing = BUILTIN_RU_SYNONYMS[normalized][pos] ?? [];
				BUILTIN_RU_SYNONYMS[normalized][pos] = [
					...existing,
					...synonyms.filter((s) => !existing.includes(s)),
				];
			}
		}
	}
}
